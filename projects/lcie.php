            <h2 class="detail-project">objective</h2>
            <p>LCIE is een platform gericht op student-ondernemers, in samenwerking met de KULeuven.
                Zij hebben als doel om leuvense studenten alle oppertuniteiten aan te bieden om in het ondernemerschap
                te stappen. Maar vaak zagen de studenten door de bomen het bos niet meer.
                Ze verloren hun ondernemerzin in een jungle van informatie, exact het omgekeerde van wat LCIE
                beoogde</p>
            <p>Daarom heeft LCIE, in samenwerking met Designosource, een complete herstructurering van de website
                uitgewerkt. </p>
        </div>
